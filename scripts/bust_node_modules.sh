#!/bin/bash

# Function to delete node_modules directories
delete_node_modules() {
  # Find all node_modules directories and delete them
  find "$1" -type d -name "node_modules" -exec rm -rf {} +
  echo "Deleted all node_modules directories in $1 and its subdirectories."
}

# Check if a directory path is provided as an argument
if [ -z "$1" ]; then
  echo "Usage: $0 <directory>"
  exit 1
fi

# Check if the provided path is a valid directory
if [ ! -d "$1" ]; then
  echo "Error: '$1' is not a valid directory."
  exit 1
fi

# Call the function to delete node_modules directories
delete_node_modules "$1"
